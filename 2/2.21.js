// Это не рабочий листинг, просто пример синтаксиса
// eventName - название события и всегда строка
// eventHandler - функция, обработчик события. Можно передать или ссылку
//  или же написать тело функции
// isEventBubble - флаг, показывающий, всплывает ли событие

source.addEventListener("eventName", eventHandler, isEventBubble);

// несмотря на то, что в начале книги заявлено, что все листинги рабочие
// этот является псевдолистингом и для корректной работы
// нуждается в небольшой коррекции